# Function to decode username
def IpUnDecode(Username:str, Debug:bool = False):
    """Decode username generated by IpUnCreate function into a IP address.

    Args:
        Username (String): Username generated by IpUnCreate function.  

    Returns:
        String: Ip of username
    """
    if Debug: print("Debugging active")
    # Split Username input into an array
    _VAray = Username.lower().split("-")
    if Debug: print(_VAray)
    
    # Turns _VArray into individual variables while decoding the username
    
    try:
        #Decode username
        _V1 = {i for i in NameLibrary if NameLibrary[i]==_VAray[0]}
        _V2 = {i for i in NameLibrary if NameLibrary[i]==_VAray[1]}
        _V3 = {i for i in NameLibrary if NameLibrary[i]==_VAray[2]}
        _V4 = {i for i in NameLibrary if NameLibrary[i]==_VAray[3]}
        
        if Debug: print("Raw values", _V1, _V2, _V3, _V4)
        
        _V1 = int(str(_V1).strip("{ }"))
        _V2 = int(str(_V2).strip("{ }"))
        _V3 = int(str(_V3).strip("{ }"))
        _V4 = int(str(_V4).strip("{ }"))
        
        if Debug: print("Stripped values:", _V1, _V2, _V3, _V4)
    except:
    
        print("ERROR: Username {} not correct. Check spelling".format(Username))
        return None
    
    #Combine Ip variables into the _Ip spring
    _Ip = "{}.{}.{}.{}".format(_V1,_V2,_V3,_V4)
    if Debug: print("IP = {}".format(_Ip))
    return _Ip